DROP TABLE IF EXISTS ACCOUNTS;

CREATE TABLE ACCOUNTS(ACC_NUMBER INT PRIMARY KEY,
   ACC_NAME VARCHAR(150),
   ACC_TYPE VARCHAR(15),
   BALANCE_DATE DATE DEFAULT NULL,
   CURRENCY VARCHAR(5),
   BALANCE DOUBLE(2) default 0);

DROP TABLE IF EXISTS TRANSACTIONS;

CREATE TABLE TRANSACTIONS(TRANSACTION_ID INT PRIMARY KEY,
  ACC_NUMBER INT,
  ACC_NAME VARCHAR(150),
  VALUE_DATE DATE,
  CURRENCY VARCHAR(5),
  DEBIT_AMOUNT DOUBLE(2) default 0,
  CREDIT_AMOUNT DOUBLE(2) default 0,
  DEBIT_CREDIT_INDICATOR VARCHAR(8),
  TRANS_NARRATIVE VARCHAR(15) null);

